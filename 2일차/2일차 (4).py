# 두 값을 입력받아, 그 값에 대한 사칙연산값을 출력하는 코드

a = int(input())
b = int(input())

print (a, '+', b, '=', a + b)
print (a, '-', b, '=', a - b)
print (b, '-', a, '=', b - a)
print (a, '*', b, '=', a * b)
print (a, '/', b, '=', a / b)
print (b, '/', a, '=', b / a)
print (a, '%', b, '=', a % b)
print (b, '%', a, '=', b % a)
