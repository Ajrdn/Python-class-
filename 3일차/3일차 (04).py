# 두 수를 값으로 비교한 결과와 객체로 비교한 결과를 출력하는 코드

a = float(input())
b = float(input())

print (f'{a == b}')
print (f'{a != b}')
print(f'{a > b}')
print(f'{a < b}')
print(f'{a >= b}')
print(f'{a <= b}')
print (f'{a is b}')
print (f'{a is not b}')
print (f'{a is a}')
print (f'{a is not a}')
print (f'{b is b}')
print (f'{b is not b}')
